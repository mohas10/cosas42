#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int	i;
	char	*string;

	i = 0;
	string = (char *)s;
	if (n != 0)
		while (i < n)
		{
			string[i] = '\0';
			i++;
		}
}
